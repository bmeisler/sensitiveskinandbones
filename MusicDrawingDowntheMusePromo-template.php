            <p> David West's new book <em>Music: Drawing Down the Muse</em> is a survey of drawings done in situ (usually in rehearsal or soundcheck) with various musicians or groups, over the last 20 years. The idea of the series was to define what the experience of the music "felt like," and to capture the emotional dynamics of each different situation. Some of these groups still exist, and others existed for a short period of time, or were "supergroups" that were entirely a one-shot deal. The series began in San Francisco in 1990 with Monte Cazazza's "Love Force," and continued during the 90s in New York, defining an epoch and an interest in music with a narrative structure. Over the last 10 years in Paris, the focus has changed to the international – with the inclusion of many French and European musicians. From Wire to Patti Smith, from the Bush Tetras to Daniel Darc, and from Devo to Silver Apples, West has strived to express the effect of the musical charge . . . often working with musicians over several days, and with the cover of invisibility he creates . . .

