            <p>Born in Detroit, <strong>David West</strong> lives and works in Paris. Over the past 30 years, he has repeatedly found himself at the epicentre of a number of burgeoning underground music and art scenes; first in Chicago, then New York and San Francisco.
            <p>Throughout his career David West has been in dialogue with seminal figures in the art world such as William S. Burroughs, David Wojnarowicz, Karen Finley, Tony Oursler to name just a few yet he paints with a voice that is very much his own. He came to portraiture when he moved to San Francisco in the early 80’s and recorded in vivid ink and watercolor, the artists and musicians that moved in and out of his life. A recent monograph, DAVID WEST 1989-2001 beautifully showcases this intimate and singular world.
            <p>His work has appeared in Nuke, k48, DOUBLE, PAPER, JHON, NY Press, Seconds, Extra small, No Milk Today, Rouge Gorge and countless other publications. He has also created album cover art, lithographs and etchings and his work has also been exhibited in galleries in Europe and the United States. His latest exhibitions were a two person show with San Francisco artist Donald Mitchell at Le Galerie Impaire in Paris, a one-man show at Les Voutes in Paris, and at the Klang and Kruger gallery in Berlin.</p>
