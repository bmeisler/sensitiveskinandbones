        <p>We were totally unprepared for the winter of 1968; it was bleak and cold, and it seemed to last forever.  My wife and I were from the North -- Reading, Pennsylvania -- and we had joined a government program to help organize the dispossessed so they could eventually help themselves. This program was called VISTA, which stands for Volunteers In Service To America.  What it really was was my ticket out of Vietnam – if I volunteered to help the poor, then I wouldn’t get drafted – it was called ‘alternative service.’
        <p>The folks we were supposed to be helping were Southerners.  Poor Southerner whites.  Rednecks.  After training in Atlanta, we got sent to Tennessee, to the tri-cities area on the Tennessee/ Virginia border – the three cities were Bristol, Kingsport and Johnson City.  We ended up in a hollow just outside the Johnson City city limits.  The locals had named this particular hollow ‘Maupin Row,’ and it was a sad collection of dilapidated wooden shanties lining several narrow dirt roads which were clustered around a polluted creek. If you were driving on the city road that circled Maupin Row you couldn’t actually see it – you had turn off the main drag and cut through a sort of high hedgerow – <a href="<?php bloginfo('url'); ?>/maupin-row" title="Maupin Row">the ‘community’ such as it was, then opened out below you.</a>
