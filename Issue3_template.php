         <p>“What you gonna be when you blow up? I bet you gonna be a balloon…kee kee kee!” That was Willie T’s salutation these days -- delivered with an eye-rolling guffaw registering satisfaction with his chortling delivery of this epistilatory greeting. He had a whole stack of them, borrowed from some commercial he’d seen on t.v. and gussied up with his own punch line that he offered up like Hallmark Cards to any and all he met, shuffling them every week or so like homilies before suddenly becoming serious, conspiratorial -- as he made his pitch for a dollar or even a quarter. Refused, he became piteous and, with an abject tug of a sleeve or urgent tap on the arm of the person he was accosting, would produce from a pocket some meaningless trinket, a bauble of costume jewelry or a fake medallion he would become proud again offering.
     <p>“Take it,” he’d say, his six-foot lanky frame bent at the neck like a condor in an effort to make meaningful eye contact. <a href="<?php bloginfo('url'); ?>/balloons-beads" title="Balloons, Beads">“Go on, take it -- it's yours.”</a>
